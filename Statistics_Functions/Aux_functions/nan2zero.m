function vec = nan2zero( vec, val )
% ZERO2NAN(vec) sets all of the nans to zeros.
%--------------------------------------------------------------------------
% ARGUMENTS
% vec       A vector.
%--------------------------------------------------------------------------
% OUTPUT
% 
%--------------------------------------------------------------------------
% EXAMPLES
% nan2zero([1,5.1,0, nan, 0.0001])
%--------------------------------------------------------------------------
% AUTHOR: Samuel Davenport
%--------------------------------------------------------------------------

if ~exist('val', 'var')
    val = 0;
end

vec(isnan(vec)) = val;

end

